#include "currency.h"

// Converts USD to EUR
float usd_to_eur(float usd) {
    return usd * 0.85; // Example conversion rate
}

// Converts EUR to USD
float eur_to_usd(float eur) {
    return eur / 0.85; // Example conversion rate
}

// Converts USD to GBP
float usd_to_gbp(float usd) {
    return usd * 0.72; // Example conversion rate
}

// Converts GBP to USD
float gbp_to_usd(float gbp) {
    return gbp / 0.72; // Example conversion rate
}